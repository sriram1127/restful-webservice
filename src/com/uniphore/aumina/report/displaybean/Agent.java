package com.uniphore.aumina.report.displaybean;

public class Agent {

	private String agentName;

	private String agentId;

	private String agentTruePercentage;

	/**
	 * @return the agentName
	 */
	public String getAgentName() {
		return agentName;
	}

	/**
	 * @param agentName
	 *            the agentName to set
	 */
	public void setAgentName(String agentName) {
		this.agentName = agentName;
	}

	/**
	 * @return the agentId
	 */
	public String getAgentId() {
		return agentId;
	}

	/**
	 * @param agentId
	 *            the agentId to set
	 */
	public void setAgentId(String agentId) {
		this.agentId = agentId;
	}

	/**
	 * @return the agentTruePercentage
	 */
	public String getAgentTruePercentage() {
		return agentTruePercentage;
	}

	/**
	 * @param agentTruePercentage
	 *            the agentTruePercentage to set
	 */
	public void setAgentTruePercentage(String agentTruePercentage) {
		this.agentTruePercentage = agentTruePercentage;
	}

}
